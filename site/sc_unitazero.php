<h2 class="news">Unit&agrave; Zero</h2>

<p>

Unit&agrave; Zero is a project by <a href="http://02l.net">02l</a>. It is 
composed of 5 stompable platforms. Such elements represent the sensitive 
nodes of the system, triggerable by means of one pressure, exercised generally 
with feet or hands, from the visitor. The activation of the platform triggers 
a chain reaction on three different levels:

<ol>
<li> local level (light feedback): represented by a pulsating, red flashlight
emitted by four LED strips located under the transparent surface of the 
platform. This blinking light is the first reply perceived by the visitor 
about the activation of the trigger.
<li> visual level (video sequencing): every platform acts as a push button: 
on or off. Therefore there are up to 32 possible combinations: each one is 
codified with a specific video, which is played back on the display system.
<li> auditory level (audio mix): in addition to video, each platform triggers 
his own audio sample. When more than one platform is triggered, each sample 
is mixed together and the result is a composite looping sound which is always
in synch.
</ol>

